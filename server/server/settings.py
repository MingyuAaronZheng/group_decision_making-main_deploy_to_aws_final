# Logging configuration
def get_logging_config():
    """Return the appropriate logging configuration based on the environment."""
    if DEBUG:
        # In development, only log from the main process to avoid duplicates
        if os.environ.get('RUN_MAIN') or not os.environ.get('WERKZEUG_RUN_MAIN'):
            console_handler = 'console'
        else:
            console_handler = 'null'
    else:
        # In production, log everything
        console_handler = 'console'

    return {
        'version': 1,
        'disable_existing_loggers': False,
        'formatters': {
            'verbose': {
                'format': '{levelname} {asctime} {module} {message}',
                'style': '{',
            },
            'detailed': {
                'format': '{asctime} - {name} - {levelname} - {message}',
                'style': '{',
            },
        },
        'handlers': {
            'null': {
                'class': 'logging.NullHandler',
            },
            'console': {
                'class': 'logging.StreamHandler',
                'formatter': 'verbose',
            },
            'file': {
                'level': 'INFO',
                'class': 'logging.handlers.RotatingFileHandler',
                'filename': os.path.join(LOG_DIR, 'django.log'),
                'maxBytes': 1024*1024*5,  # 5 MB
                'backupCount': 5,
                'formatter': 'detailed',
                'encoding': 'utf8',
            },
        },
        'root': {
            'handlers': [console_handler, 'file'],
            'level': 'INFO',
        },
        'loggers': {
            'experiment': {
                'handlers': [console_handler, 'file'],
                'level': 'INFO',
                'propagate': False,
            },
            'channels': {
                'handlers': [console_handler, 'file'],
                'level': 'DEBUG' if DEBUG else 'INFO',
                'propagate': False,
            },
            'django': {
                'handlers': [console_handler, 'file'],
                'level': 'INFO',
                'propagate': False,
            },
        },
    }

# Apply the logging configuration
LOGGING = get_logging_config()
